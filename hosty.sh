# --- helpers -----------------------------------------------------------------

# Tracked temps are removed on EXIT/INT/TERM. Debug OUTPUT_HOSTS is not tracked
# so the built hosts file remains for inspection after the run.
TEMPS=""

cleanup_temps() {
    for _tmp in $TEMPS; do
        rm -f "$_tmp" 2> /dev/null || true
    done
}

trap cleanup_temps EXIT INT TERM

add_temp() {
    TEMPS="$TEMPS $1"
}

mktemp_file() {
    _t=$(mktemp) || exit 1
    add_temp "$_t"
    printf '%s\n' "$_t"
}

checkDep() {
    command -v "$1" > /dev/null 2>&1 || {
        echo >&2 "hosty requires '$1' but it's not installed."
        exit 1
    }
}

is_yes() {
    case $1 in
        y | Y | yes | YES) return 0 ;;
        *) return 1 ;;
    esac
}

is_no() {
    case $1 in
        n | N | no | NO) return 0 ;;
        *) return 1 ;;
    esac
}

# Read one line from stdin (TTY or pipe); fall back to the controlling TTY.
# Prints the line to stdout (without relying on nameref / eval).
read_line() {
    if read -r _line; then
        printf '%s\n' "$_line"
        return 0
    fi
    if [ -r /dev/tty ] && read -r _line < /dev/tty; then
        printf '%s\n' "$_line"
        return 0
    fi
    echo "failed to read input." >&2
    return 1
}

remove_cron_scripts() {
    for _period in daily weekly monthly; do
        _f="/etc/cron.$_period/hosty"
        if [ -f "$_f" ]; then
            echo "removing $_f..."
            rm -f "$_f"
            echo
        fi
    done
}

# Atomically replace destination with source contents (same-dir temp + mv).
install_hosts_file() {
    _src=$1
    _dir=$(dirname "$OUTPUT_HOSTS")
    _tmp=$(mktemp "$_dir/.hosty.XXXXXX") || exit 1
    add_temp "$_tmp"
    cat "$_src" > "$_tmp"
    mv -f "$_tmp" "$OUTPUT_HOSTS"
}

# Required download (default source lists). Exits on failure.
# Sets tmp_download_file to the path of the downloaded content.
downloadFile() {
    tmp_download_file=$(mktemp_file)
    echo "downloading $1"
    if ! curl -sSL --retry 3 -o "$tmp_download_file" "$1"; then
        echo "error downloading $1"
        exit 1
    fi
}

# Optional download (per-list entries). Continues on failure.
# Sets tmp_download_file on success; clears it on failure.
downloadList() {
    tmp_download_file=$(mktemp_file)
    echo "downloading $1"
    if ! curl -sSL --retry 1 --max-time 10 -o "$tmp_download_file" "$1"; then
        echo "error downloading $1"
        rm -f "$tmp_download_file"
        tmp_download_file=""
        return 1
    fi
}

# Append each non-empty, non-comment URL from a sources file into dest via downloadList.
downloadSourcesInto() {
    _sources=$1
    _dest=$2
    while read -r line || [ -n "$line" ]; do
        # Skip blanks and comment lines
        case $line in
            '' | \#*) continue ;;
        esac
        if downloadList "$line"; then
            cat "$tmp_download_file" >> "$_dest"
        fi
    done < "$_sources"
}

# Extract hostnames from mixed hosts/domain/list formats into $1 (sorted unique).
extractDomains() {
    echo
    echo "extracting domains..."
    tmp_domains=$(mktemp_file)
    awk '
        /^\s*[a-zA-Z0-9:]/ {
            line = $0
            sub(/#.*/, "", line)
            gsub(/[^a-zA-Z0-9.\-]/, "\n", line)
            n = split(line, parts, "\n")
            for (i = 1; i <= n; i++) {
                d = parts[i]
                if (d ~ /\./ && d ~ /[a-zA-Z]/ && d !~ /^[.\-]/ && d !~ /[.\-]$/)
                    print d
            }
        }
    ' "$1" | sort -u > "$tmp_domains"
    cat "$tmp_domains" > "$1"
    domains_counter=$(awk 'END { print NR + 0 }' "$1")
    echo "$domains_counter domains extracted."
}

# Trim leading/trailing empty lines from a file into stdout.
trim_empty_lines() {
    awk 'NR == FNR {
        if (NF) {
            if (!beg) beg = NR
            end = NR
        }
        next
    }
    FNR >= beg && FNR <= end' "$1" "$1"
}

# --- main --------------------------------------------------------------------

parse "$@"
eval "set -- $REST"

# check dependencies
checkDep curl
checkDep awk
checkDep head
checkDep cat
checkDep mktemp
checkDep sort
checkDep grep

echo "======== hosty v$VERSION ($RELEASE_DATE) ========"
echo "========       $PROJECT_URL       ========"
echo

# avoid all system changes if debug mode is enabled
if [ "$DEBUG" = 1 ]; then
    AUTORUN=""
    UNINSTALL=""
    # Not tracked: left on disk after exit for inspection (CI and users rely on this).
    OUTPUT_HOSTS=$(mktemp)
    echo "******** DEBUG MODE ON ********"
    echo
fi

# check if running as root
if [ "$(id -u)" != 0 ] && [ "$DEBUG" != 1 ]; then
    echo "please run as root."
    exit 1
fi

# --uninstall option
if [ "$UNINSTALL" = 1 ]; then
    if [ -d /etc/hosty ]; then
        echo "do you want to remove /etc/hosty configs directory? y/n"
        answer=$(read_line)
        echo

        if is_yes "$answer"; then
            echo "removing hosty configs directory..."
            rm -R /etc/hosty
            echo
        elif ! is_no "$answer"; then
            echo "bad answer, exiting..."
            exit 1
        fi
    fi

    remove_cron_scripts

    previous_crontab=$(mktemp_file)
    if crontab -l 2> /dev/null > "$previous_crontab" && grep "/usr/local/bin/hosty" "$previous_crontab" > /dev/null 2>&1; then
        echo "removing from crontab..."
        new_crontab=$(mktemp_file)
        awk '!/\/usr\/local\/bin\/hosty/' "$previous_crontab" > "$new_crontab"
        crontab "$new_crontab"
        echo
    fi

    if [ -f /usr/local/bin/hosty ]; then
        echo "uninstalling hosty..."
        rm /usr/local/bin/hosty
        echo
    fi

    echo "hosty uninstalled."
    exit 0
fi

# copy original hosts file and handle --restore
user_hosts_file=$(mktemp_file)
user_hosts_line_number=$(awk '
    /^# [aA]d blocking hosts generated/ { counter = NR }
    END {
        if (!counter) print -1
        else print counter - 1
    }
' "$INPUT_HOSTS")

# if hosty has never been executed, don't restore anything
if [ "$user_hosts_line_number" -lt 0 ]; then
    if [ "$RESTORE" = 1 ]; then
        echo "there is nothing to restore."
        exit 0
    fi

    # if it's the first time running hosty, save the whole /etc/hosts file
    cat "$INPUT_HOSTS" > "$user_hosts_file"
else
    # copy original hosts lines (empty when marker is on line 1)
    if [ "$user_hosts_line_number" -gt 0 ]; then
        head -n "$user_hosts_line_number" "$INPUT_HOSTS" > "$user_hosts_file"
    else
        : > "$user_hosts_file"
    fi

    # if --restore is present, restore original hosts and exit
    if [ "$RESTORE" = 1 ]; then
        restored_hosts=$(mktemp_file)
        trim_empty_lines "$user_hosts_file" > "$restored_hosts"
        install_hosts_file "$restored_hosts"
        echo "$OUTPUT_HOSTS restore completed."
        exit 0
    fi
fi

# cron options
if [ "$AUTORUN" = 1 ]; then
    echo "configuring autorun..."

    checkDep crontab

    # remove legacy cron.* scripts if present
    for _period in daily weekly monthly; do
        _f="/etc/cron.$_period/hosty"
        if [ -f "$_f" ]; then
            echo
            echo "removing $_f..."
            rm -f "$_f"
        fi
    done

    # if user passed --ignore-default-sources, autorun with that
    if [ "$IGNORE_DEFAULT_SOURCES" != 1 ]; then
        hosty_cmd="/usr/local/bin/hosty"
    else
        echo
        echo "autorunning with --ignore-default-sources..."
        hosty_cmd="/usr/local/bin/hosty -i"
    fi

    echo
    echo "how often do you want to run hosty automatically?"
    echo "enter 'daily', 'weekly', 'monthly' or 'never':"
    period=$(read_line)

    previous_crontab=$(mktemp_file)
    (crontab -l 2> /dev/null || true) > "$previous_crontab"
    new_crontab=$(mktemp_file)
    awk '!/\/usr\/local\/bin\/hosty/' "$previous_crontab" > "$new_crontab"

    case $period in
        daily)
            echo "0 0 * * * $hosty_cmd" >> "$new_crontab"
            crontab "$new_crontab"
            ;;
        weekly)
            echo "0 0 * * 0 $hosty_cmd" >> "$new_crontab"
            crontab "$new_crontab"
            ;;
        monthly)
            echo "0 0 1 * * $hosty_cmd" >> "$new_crontab"
            crontab "$new_crontab"
            ;;
        never)
            if grep "/usr/local/bin/hosty" "$previous_crontab" > /dev/null 2>&1; then
                crontab "$new_crontab"
            fi
            ;;
        *)
            echo
            echo "bad answer, exiting..."
            exit 1
            ;;
    esac

    echo
    echo "done."
    exit 0
fi

blacklist_sources=$(mktemp_file)
whitelist_sources=$(mktemp_file)

# remove default sources if the user wants that
if [ "$IGNORE_DEFAULT_SOURCES" != 1 ]; then
    echo "downloading default sources..."

    downloadFile "$BLACKLIST_DEFAULT_SOURCE"
    cat "$tmp_download_file" >> "$blacklist_sources"

    downloadFile "$WHITELIST_DEFAULT_SOURCE"
    cat "$tmp_download_file" >> "$whitelist_sources"
fi

# user custom blacklist sources
if [ -f /etc/hosty/blacklist.sources ]; then
    echo
    echo "adding custom blacklist sources..."
    cat /etc/hosty/blacklist.sources >> "$blacklist_sources"
fi

# user custom whitelist sources
if [ -f /etc/hosty/whitelist.sources ]; then
    echo
    echo "adding custom whitelist sources..."
    cat /etc/hosty/whitelist.sources >> "$whitelist_sources"
fi

echo
echo "downloading blacklists..."
blacklist_domains=$(mktemp_file)
downloadSourcesInto "$blacklist_sources" "$blacklist_domains"

if [ -f /etc/hosty/blacklist ]; then
    echo
    echo "applying user custom blacklist..."
    cat "/etc/hosty/blacklist" >> "$blacklist_domains"
fi

extractDomains "$blacklist_domains"

echo
echo "downloading whitelists..."
whitelist_domains=$(mktemp_file)
downloadSourcesInto "$whitelist_sources" "$whitelist_domains"

if [ -f /etc/hosty/whitelist ]; then
    echo
    echo "applying user custom whitelist..."
    cat "/etc/hosty/whitelist" >> "$whitelist_domains"
fi

# whitelist sources and original hosts file domains
cat "$blacklist_sources" "$whitelist_sources" "$user_hosts_file" >> "$whitelist_domains"

extractDomains "$whitelist_domains"

echo
echo "building $OUTPUT_HOSTS"
final_hosts_file=$(mktemp_file)

trim_empty_lines "$user_hosts_file" > "$final_hosts_file"

# add blank line and hosty marker
{
    echo
    echo "# Ad blocking hosts generated $(date)"
    echo "# Don't write below this line. It will be lost if you run hosty again."
} >> "$final_hosts_file"

echo
echo "cleaning and de-duplicating..."

# apply whitelist and dedup blacklist entries
awk -v ip="$BLOCK_IP" 'FNR == NR { arr[$1]++ } FNR != NR { if (!arr[$1]++) print ip, $1 }' \
    "$whitelist_domains" "$blacklist_domains" >> "$final_hosts_file"

# count websites blocked (fixed-string match on first field)
websites_blocked_counter=$(awk -v ip="$BLOCK_IP" '$1 == ip { count++ } END { print count + 0 }' "$final_hosts_file")

install_hosts_file "$final_hosts_file"

echo
echo "done, $websites_blocked_counter websites blocked."
echo
echo "you can always restore your original hosts file with this command:"
echo "$ sudo hosty -r (--restore)"
