AUTORUN=''
IGNORE_DEFAULT_SOURCES=''
RESTORE=''
DEBUG=''
UNINSTALL=''
REST=''
parse() {
    OPTIND=$(($# + 1))
    while OPTARG= && [ $# -gt 0 ]; do
        case $1 in
            --?*=*)
                OPTARG=$1
                shift
                eval 'set -- "${OPTARG%%\=*}" "${OPTARG#*\=}"' ${1+'"$@"'}
                ;;
            --no-* | --without-*) unset OPTARG ;;
            -[airduhv]?*)
                OPTARG=$1
                shift
                eval 'set -- "${OPTARG%"${OPTARG#??}"}" -"${OPTARG#??}"' ${1+'"$@"'}
                OPTARG=
                ;;
        esac
        case $1 in
            '-a' | '--autorun')
                [ "${OPTARG:-}" ] && OPTARG=${OPTARG#*\=} && set "noarg" "$1" && break
                eval '[ ${OPTARG+x} ] &&:' && OPTARG='1' || OPTARG=''
                AUTORUN="$OPTARG"
                ;;
            '-i' | '--ignore-default-sources')
                [ "${OPTARG:-}" ] && OPTARG=${OPTARG#*\=} && set "noarg" "$1" && break
                eval '[ ${OPTARG+x} ] &&:' && OPTARG='1' || OPTARG=''
                IGNORE_DEFAULT_SOURCES="$OPTARG"
                ;;
            '-r' | '--restore')
                [ "${OPTARG:-}" ] && OPTARG=${OPTARG#*\=} && set "noarg" "$1" && break
                eval '[ ${OPTARG+x} ] &&:' && OPTARG='1' || OPTARG=''
                RESTORE="$OPTARG"
                ;;
            '-d' | '--debug')
                [ "${OPTARG:-}" ] && OPTARG=${OPTARG#*\=} && set "noarg" "$1" && break
                eval '[ ${OPTARG+x} ] &&:' && OPTARG='1' || OPTARG=''
                DEBUG="$OPTARG"
                ;;
            '-u' | '--uninstall')
                [ "${OPTARG:-}" ] && OPTARG=${OPTARG#*\=} && set "noarg" "$1" && break
                eval '[ ${OPTARG+x} ] &&:' && OPTARG='1' || OPTARG=''
                UNINSTALL="$OPTARG"
                ;;
            '-h' | '--help')
                usage
                exit 0
                ;;
            '-v' | '--version')
                echo "${VERSION}"
                exit 0
                ;;
            --)
                shift
                while [ $# -gt 0 ]; do
                    REST="${REST} \"\${$((OPTIND - $#))}\""
                    shift
                done
                break
                ;;
            [-]?*)
                set "unknown" "$1"
                break
                ;;
            *)
                REST="${REST} \"\${$((OPTIND - $#))}\""
                ;;
        esac
        shift
    done
    [ $# -eq 0 ] && {
        OPTIND=1
        unset OPTARG
